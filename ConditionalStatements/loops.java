package ConditionalStatements;

public class loops {
    public static void main(String[] args) {
        System.out.println("Using for loop stars are printed");
        for(int i =0;i<5;i++){
            System.out.println("*");
        }
        System.out.println("using while loop printed stars");
        int i=0;
        while(i<5){
            System.out.println("*");
            i++;
        }
        System.out.println("Using do-while loop stars are printed");
        int m=0;
        do{
            System.out.println("*");
            m++;
        }while(m<5);

    }
    
}
