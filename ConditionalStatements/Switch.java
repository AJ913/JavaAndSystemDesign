package ConditionalStatements;

public class Switch {
    public static void main(String[] args) {
        int num =100;
        switch (num){
            case 400: System.out.println("Case 1 is matching");
            break;
            case 300:System.out.println("Case 2 is matching");
            break;
            case 500:System.out.println("Case 3 is matching ");
            break;
        
            default:System.out.println("default case");
        }
    }
    
}
